#include "LED.h"

///*******************************************************************************
//*函数名:void LED_ON(void)
//*功能:LED 开启
//*输入:无
//*输出:无
//*备注:无
//*******************************************************************************/
void LED_ON(void)
{
 GPIO_SetBits(LED_GPIO,LED_PIN);
}
///*******************************************************************************
//*函数名:void LED_OFF(void)
//*功能:LED 关闭
//*输入:无
//*输出:无
//*备注:无
//*******************************************************************************/
void LED_OFF(void)
{
 GPIO_ResetBits(LED_GPIO,LED_PIN);
}
///*******************************************************************************
//*函数名:void LED_Flicker(void)
//*功能:LED 闪烁
//*输入:无
//*输出:无
//*备注:无
//*******************************************************************************/
void LED_Flicker(void)
{
   LED_GPIO->ODR ^= LED_PIN;
}

